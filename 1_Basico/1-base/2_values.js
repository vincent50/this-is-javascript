// primitives values

// integer
let number = 40

// String
let string = 'Hello world!'

// boolean
let boolean_positive = true
let boolean_negative = false

// empty values
let empty_null = null
let empty_undefined = undefined


// object values

// Array
let array = [1, 2, 3]

// Onject
let object = { nombre: 'Vincent'}


// validate type
console.log( typeof(number) ) // number
console.log( typeof(string) ) // string
console.log( typeof(boolean_positive) ) // boolean
console.log( typeof(boolean_negative) ) // boolean
console.log( typeof(empty_null) ) // object
console.log( typeof(empty_undefined) ) // undefined
console.log( typeof(array) ) // object
console.log( typeof(object) ) // object
